package io.github.zhangsen.common;

/**
 * 平面中的形状
 */
public class PlaneShape {

    private int minX;

    private int minY;

    private int maxX;

    private int maxY;


    public int getMinX() {
        return minX;
    }

    public int getMinY() {
        return minY;
    }

    public int getMaxX() {
        return maxX;
    }

    public int getMaxY() {
        return maxY;
    }
}
